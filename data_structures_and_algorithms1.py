def count_rotation_binary(nums):
    lo = 0
    hi = len(nums) - 1

    while lo <= hi:
        mid = (lo + hi) // 2
        mid_number = nums[mid]

        # Check if mid is the rotation point
        if mid > 0 and mid_number < nums[mid - 1]:
            return mid
        elif mid_number < nums[hi]:
            hi = mid - 1
        else:
            lo = mid + 1

    return 0

