def make_balanced_bat(data, lo=0, hi=None, parent=None):
  if hi is None:
    hi = len(data) - 1
  if lo > hi:
    return None
  mid = (lo + hi) // 2
  key, value = data[mid]
  root = BSTNode(key, value)
  root.parent = parent
  root.left = make_balanced_bat(data, lo, mid-1, root)
  root.right = make_balanced_bat(data, mid+1, hi, root)
  return root

